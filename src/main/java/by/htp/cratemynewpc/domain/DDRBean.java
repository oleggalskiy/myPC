package by.htp.cratemynewpc.domain;

public class DDRBean {
    private Integer idDdr;
    private String ddrName;
    private String ddrType;
    private String ddrForm;
    private String ddrFrequency;
    private String ddrSize;
    private String ddrFabricator;

    public Integer getIdDdr() {
        return idDdr;
    }

    public void setIdDdr(Integer idDdr) {
        this.idDdr = idDdr;
    }

    public String getDdrName() {
        return ddrName;
    }

    public void setDdrName(String ddrName) {
        this.ddrName = ddrName;
    }

    public String getDdrType() {
        return ddrType;
    }

    public void setDdrType(String ddrType) {
        this.ddrType = ddrType;
    }

    public String getDdrForm() {
        return ddrForm;
    }

    public void setDdrForm(String ddrForm) {
        this.ddrForm = ddrForm;
    }

    public String getDdrFrequency() {
        return ddrFrequency;
    }

    public void setDdrFrequency(String ddrFrequency) {
        this.ddrFrequency = ddrFrequency;
    }

    public String getDdrSize() {
        return ddrSize;
    }

    public void setDdrSize(String ddrSize) {
        this.ddrSize = ddrSize;
    }

    public String getDdrFabricator() {
        return ddrFabricator;
    }

    public void setDdrFabricator(String ddrFabricator) {
        this.ddrFabricator = ddrFabricator;
    }
}
