package by.htp.cratemynewpc.domain;



public class PCBean {
    private String idPC;
    private String date;
    private String pcCPU;
    private String pcMb;
    private String pcVga;
    private String pcRam;

    public String getIdPC() {
        return idPC;
    }

    public void setIdPC(String idPC) {
        this.idPC = idPC;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPcCPU() {
        return pcCPU;
    }

    public void setPcCPU(String pcCPU) {
        this.pcCPU = pcCPU;
    }

    public String getPcMb() {
        return pcMb;
    }

    public void setPcMb(String pcMb) {
        this.pcMb = pcMb;
    }

    public String getPcVga() {
        return pcVga;
    }

    public void setPcVga(String pcVga) {
        this.pcVga = pcVga;
    }

    public String getPcRam() {
        return pcRam;
    }

    public void setPcRam(String pcRam) {
        this.pcRam = pcRam;
    }




}
