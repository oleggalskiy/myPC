package by.htp.cratemynewpc.domain;

public class CPUBean {
    private Integer idCpu;
    private String nameCPU;
    private String cpuSocket;
    private String cpuCore;
    private String cpuNumberCores;
    private String cpuProcess;
    private String cpuFrequency;
    private String cpuSystemBus;
    private String cpuFabricator;

    public Integer getIdCpu() {
        return idCpu;
    }

    public void setIdCpu(Integer idCpu) {
        this.idCpu = idCpu;
    }

    public String getNameCPU() {
        return nameCPU;
    }

    public void setNameCPU(String nameCPU) {
        this.nameCPU = nameCPU;
    }

    public String getCpuSocket() {
        return cpuSocket;
    }

    public void setCpuSocket(String cpuSocket) {
        this.cpuSocket = cpuSocket;
    }

    public String getCpuCore() {
        return cpuCore;
    }

    public void setCpuCore(String cpuCore) {
        this.cpuCore = cpuCore;
    }

    public String getCpuNumberCores() {
        return cpuNumberCores;
    }

    public void setCpuNumberCores(String cpuNumberCores) {
        this.cpuNumberCores = cpuNumberCores;
    }

    public String getCpuProcess() {
        return cpuProcess;
    }

    public void setCpuProcess(String cpuProcess) {
        this.cpuProcess = cpuProcess;
    }

    public String getCpuFrequency() {
        return cpuFrequency;
    }

    public void setCpuFrequency(String cpuFrequency) {
        this.cpuFrequency = cpuFrequency;
    }

    public String getCpuSystemBus() {
        return cpuSystemBus;
    }

    public void setCpuSystemBus(String cpuSystemBus) {
        this.cpuSystemBus = cpuSystemBus;
    }

    public String getCpuFabricator() {
        return cpuFabricator;
    }

    public void setCpuFabricator(String cpuFabricator) {
        this.cpuFabricator = cpuFabricator;
    }
}
